package JavaLoops.exercise;

public class LoopExercise1 {
	
	//program to print a number from 1-20;
	public void printLoop() {
		System.out.println("Execution of printLoop");
		System.out.println("------------------------------");
		System.out.println("Printed Numbers from 1 - 10:");
		
		for(int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
	}

}
